class Solution:
    def uniquePathsWithObstacles(self, obstacleGrid):
        """
        :type obstacleGrid: List[List[int]]
        :rtype: int
        """
        if not obstacleGrid:
            return 0

        memo = {}
        def path(row, col):
            if (row, col) not in memo:
                if obstacleGrid[row][col] == 1:
                    ans = 0
                elif row + 1 < len(obstacleGrid) and col + 1 < len(obstacleGrid[0]):
                    ans = path(row + 1, col) + path(row, col + 1)
                elif row + 1 < len(obstacleGrid):
                    ans = path(row + 1, col)
                elif col + 1 < len(obstacleGrid[0]):
                    ans = path(row, col + 1)
                elif row + 1 == len(obstacleGrid) and col + 1 == len(obstacleGrid[0]):
                    ans = 1

                memo[row, col] = ans
            return memo[row, col]

        return path(0, 0)


